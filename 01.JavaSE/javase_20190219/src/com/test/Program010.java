package com.test;

public class Program010 {

	public static void main(String[] args) {
		
		//자료형 구분
		//1. 기본 자료형(Primitive Data Types)
		//2. 참조 자료형(Reference/Object Data Types)
		

		//1. 기본 자료형(Primitive Data Types)
		//정수, 실수, 문자, 부울(논리값)
		//정수- byte, short, int, long
		//실수- float, double
		//문자- char
		//부울- boolean
		
		
		//실수- float, double
		//지수부, 가수부를 따로 운영한다.
		//가수부의 크기가 제한되어 있어서 정밀도가 제한된다. 
		
		//변수 a라는 공간에 10.1F 이라는 값 저장
		//변수 a는 float 자료형만큼의 공간(4byte)을 차지한다
		//float 자료형 값은 접미사 F를 필요로 합니다.
		float a = 10.1F;
		
		//변수 b라는 공간에 20.3 이라는 값 저장
		//변수 b는 double 자료형만큼의 공간(8byte)을 차지한다
		//double는 실수 자료형 중에서 기본 자료형 
		double b = 20.3;
		
		
	}

}
